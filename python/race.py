class Race(object):
    def __init__(
            self,
            name,
            description,
            icon_filename,
            cons_mod,
            endu_mod,
            pneu_mod,
            resi_mod,
            reso_mod,
            prov_mod,
            ):
        self.name = name
        self.description = description
        self.icon_filename = icon_filename
        self.cons_mod = cons_mod
        self.endu_mod = endu_mod
        self.pneu_mod = pneu_mod
        self.resi_mod = resi_mod
        self.reso_mod = reso_mod
        self.prov_mod = prov_mod
        
